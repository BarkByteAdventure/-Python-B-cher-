# Inventiert die Groß-/Kleinschreibung
print("Hello World!".swapcase())

# Konvertiert String in das Titelformat
print("Hello World!".title())

# Macht den Anfangsbuchstaben eines Strings zu einem Großbuchstaben
print("hello world!".capitalize())

# Macht den Buchstaben eines Strings zu einem Kleinsbuchstab
print("hello world!".lower())

# Macht den Buchstaben eines Strings zu einem Großbuchstaben
print("Hello World!".upper())


