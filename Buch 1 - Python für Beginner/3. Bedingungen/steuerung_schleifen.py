# Break-Statement:
a = 5

while(a < 10):
    print(a)
    if(a == 5):
        break
    

# Continue-Statement:

liste = [400, 23, 1, 43, "Hello"]

for i in liste:
    if(i == 23):
        continue
    print(i)
    

# Pass-Statement:

a = 0

while(a < 10):
    if(a == 5):
        pass
    print(a)
    