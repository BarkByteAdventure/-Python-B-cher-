# Sequenz Liste prüfen

liste = [1, 2, 3, 4, 5, 6]

print(2 in liste)    # gibt True zurück wenn 2 in der Liste ist

print(10 in liste)   # gibt False zurück wenn 10 in der Liste ist


# Sequenzen Dictionaries prüfen

dict1 = {1: "a", 2: "b", 3: "c"}

print(2 in dict1)    # gibt True zurück wenn 2 in der Dictionary ist

print(10 in dict1)   # gibt False zurück wenn 10 in der Dictionary ist

print("A" in dict1)   # gibt False zurück wenn "A" in der Dictionary ist
