from math import *

print(sin(35))
# Output: -0.428182669496151

print(cos(35))
# Output: -0.9036922050915067

print(tan(35))
# 0.47381472041445105

print(degrees(2))
# Output: 114.59155902616465

print(radians(150))
# Output: 2.6179938779914944

print(degrees(pi))
# Output: 180.0

print(radians(pi))
# Output: 0.05483113556160755

print(pi)
# Output: 3.14159265358979

print(factorial(6))
# Output: 720 (gerechnet: 6x5x4x3x2x1)